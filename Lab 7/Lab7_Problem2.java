import java.util.*;
public class Lab7_Problem2
{
  
  public static void main(String args[]) 
  {
    int iMonth;
    for (iMonth =1; iMonth <=12; iMonth++)
    {
      System.out.println("Month: "+iMonth);
      fvDay(iMonth);
    }
  }
  
  
  public static void fvDay(int piMonth)
  {
    int iDay;
    if (piMonth==2)
    {
      for(iDay =1; iDay <=28; iDay++)
      {
        System.out.println("\tDay: "+iDay);
      }
    }
    else if (piMonth==1 || piMonth==3 || piMonth==5 || piMonth==7 || piMonth==8 || piMonth==10 || piMonth==12)
    {
      for(iDay =1; iDay <=31; iDay++)
      {
        System.out.println("\tDay: "+iDay);
      }
    }
    else
    {
      for(iDay =1; iDay <=30; iDay++)
      {
        System.out.println("\tDay: "+iDay);
      }
    }
       
  }
}